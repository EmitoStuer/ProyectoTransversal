    private boolean comprobarCaracteres(String e){
        boolean validado=true;
        for (int i =0; i<e.length();i++){
            char c = e.charAt(i);
            if (!Character.isLetter(c) && !Character.isWhitespace(c)){
                validado=  false;
                break;
            }
        }
        return validado;
    }

